def _compute_tier(total_ratings: int) -> str:
    """Derive tier from total lifetime ratings."""
    if total_ratings >= 100:
        return "elite"
    if total_ratings >= 30:
        return "scout"
    if total_ratings >= 10:
        return "regular"
    return "newbie"


async def get_scout_identity(user: dict) -> dict:
    """
    Return the full identity context for a user.

    Called on login, signup, and profile refresh.
    The frontend uses `onboarding` to route to the correct flow,
    and `capabilities` to gate features without hardcoding tier logic.
    """
    total_ratings = user.get("total_ratings", 0)
    tier = _compute_tier(total_ratings)
    tier_meta = SCOUT_TIERS[tier]

    # Network contribution stats
    now = datetime.now(timezone.utc)
    week_ago = now - timedelta(days=7)
    ratings_7d = await db.ratings.count_documents({
        "user_id": user["id"],
        "timestamp": {"$gte": week_ago},
    })

    # Venues this scout has contributed signal to (lifetime)
    venues_rated = await db.ratings.distinct("venue_id", {"user_id": user["id"]})

    # Next tier progress
    next_tier = None
    ratings_to_next = None
    if tier != "elite":
        tier_order = ["newbie", "regular", "scout", "elite"]
        next_tier_key = tier_order[tier_order.index(tier) + 1]
        next_tier_meta = SCOUT_TIERS[next_tier_key]
        ratings_to_next = next_tier_meta["min_ratings"] - total_ratings
        next_tier = next_tier_key

    # Merchant / admin overrides to the role
    role = tier_meta["role"]
    if user.get("is_merchant"):
        role = "operator"
    if user.get("is_admin") or user.get("is_super_admin"):
        role = "controller"

    return {
        "tier":            tier,
        "tier_label":      tier_meta["label"],
        "role":            role,
        "onboarding":      tier_meta["onboarding"],
        "capabilities":    tier_meta["capabilities"],
        "signal_weight":   tier_meta["signal_weight"],
        "credibility":     tier_meta["credibility"],
        "description":     tier_meta["description"],
        # ── Contribution stats ────────────────────────────────────────
        "total_ratings":   total_ratings,
        "ratings_7d":      ratings_7d,
        "venues_contributed": len(venues_rated),
        # ── Progression ───────────────────────────────────────────────
        "next_tier":         next_tier,
        "ratings_to_next":   ratings_to_next,
        # ── Subscription ─────────────────────────────────────────────
        "is_vibe_plus":    user.get("is_vibe_plus", False),
        "icon_tier":       user.get("icon_tier"),
    }
