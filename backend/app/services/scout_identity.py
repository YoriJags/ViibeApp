"""
Viibe - Scout Identity Service

Scouts are the sensor network — not power users, not a feature.
Every user in the system has a role relative to the data hierarchy:

  SENSOR (Scout)     — generates signal. The hardware layer.
  CONSUMER           — reads signal generated by scouts.
  OPERATOR (Merchant)— owns a venue node in the network.
  CONTROLLER (Admin) — manages network integrity.

This module is the authoritative source for:
  - What tier a user occupies (newbie → regular → scout → elite)
  - What capabilities their tier unlocks
  - What their contribution means to network signal quality
  - Which onboarding path the frontend should route them to

Designed so the frontend never needs to make role decisions itself —
it asks for the identity context and acts on what it receives.
"""
